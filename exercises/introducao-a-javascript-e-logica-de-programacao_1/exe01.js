
function sum(valueOne, ValueTwo) {
  let result = valueOne + ValueTwo;
  return result;
}

function sub(valueOne, ValueTwo) {
  let result = valueOne - ValueTwo;
  return result;
}

function mul(valueOne, ValueTwo) {
  let result = valueOne * ValueTwo;
  return result;
}

function div(valueOne, ValueTwo) {
  let result = valueOne / ValueTwo;
  return result;
}

